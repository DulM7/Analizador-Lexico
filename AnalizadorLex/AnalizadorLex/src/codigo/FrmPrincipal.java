    private void btnAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarActionPerformed
       JFileChooser fileChooser = new JFileChooser(); // Crear un nuevo JFileChooser
    int seleccion = fileChooser.showOpenDialog(this); // Mostrar el diálogo de selección de archivo

    if (seleccion == JFileChooser.APPROVE_OPTION) { // Si el usuario selecciona un archivo
        File archivo = fileChooser.getSelectedFile(); // Obtener el archivo seleccionado
        try {
            // Abrir el archivo para lectura
            Reader lector = new BufferedReader(new FileReader(archivo));
            Lexer lexer = new Lexer(lector);
            String resultado = "";
            while (true) {
                Tokens tokens = lexer.yylex();
                if (tokens == null) {
                    resultado += "FIN";
                    txtResultado.setText(resultado);
                    return;
                }
                   
                
                switch (tokens) {
                    case ERROR:
                        resultado += "Símbolo no definido\n";
                        break;
                    case Identificador:
                        resultado += lexer.lexeme + ": Es un Identificador\n";
                        break;
                    case Entero:
                        resultado += lexer.lexeme + ": Es un Número Entero\n";
                        break;
                    case Real:
                        resultado += lexer.lexeme + ": Es un Número Real\n";
                        break;
                    case Reservadas:
                        resultado += lexer.lexeme + ": Es una palabra reservada\n";
                        break;
                    default:
                        resultado += "Token: " + tokens + "\n";
                        break;
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    }//GEN-LAST:event_btnAnalizarActionPerformed

